// ============================================================================
// 按键处理模块
// ============================================================================
// 本模块负责将 Windows 虚拟键码（Virtual Key Code）转换为可读的字符串
//
// Windows 虚拟键码是 Windows 系统为每个按键分配的唯一标识符
// 例如：
//   - A 键的虚拟键码是 0x41 (65)
//   - Enter 键的虚拟键码是 0x0D (13)
//   - F1 键的虚拟键码是 0x70 (112)
//
// 本模块处理：
// 1. 特殊按键（Shift、Ctrl、Alt、方向键等）的识别
// 2. 修饰键状态（Shift、Caps Lock）的跟踪
// 3. 普通字符按键的转换（使用 Windows API ToAscii）
// ============================================================================

use crate::types::*; // 导入类型定义，包含各种虚拟键码常量

// Hook 方式需要使用 Windows API 来获取键盘状态和转换字符
#[cfg(feature = "hook")]
use crate::windows_api::{g_GetKeyboardState, g_ToAscii};

// ============================================================================
// 全局状态变量
// ============================================================================
// 这些变量用于跟踪修饰键的状态
// 使用 static mut 表示可变的全局静态变量，访问时需要 unsafe 块

/// Shift 键是否被按下
/// 用于判断是否应该输出大写字母或符号
static mut SHIFT_PRESSED: bool = false;

/// Caps Lock 是否开启
/// 用于判断字母是否应该大写
static mut CAPS_LOCK_ON: bool = false;

/// 将虚拟键码转换为可读的字符串
///
/// 这是本模块的核心函数，负责将 Windows 虚拟键码转换为人类可读的字符串
///
/// # 参数
/// * `vk_code` - Windows 虚拟键码（0-255）
/// * `is_keydown` - 是否是按键按下事件（true=按下，false=释放）
///
/// # 返回
/// 转换后的字符串，例如：
/// - 普通字母：返回 "a"、"b"、"c" 等
/// - 特殊按键：返回 "[shift]"、"[ctrl]"、"[return]" 等
/// - 无法识别：返回空字符串 ""
///
/// # 示例
/// ```
/// let key_str = vk_to_string(0x41, true);  // A 键按下
/// // 如果 Shift 未按下且 Caps Lock 未开启，返回 "a"
/// // 如果 Shift 按下或 Caps Lock 开启，返回 "A"
/// ```
pub fn vk_to_string(vk_code: u32, is_keydown: bool) -> String {
    unsafe {
        // ====================================================================
        // 第一部分：处理特殊按键
        // ====================================================================
        // 这些按键有特殊含义，需要特殊处理
        match vk_code {
            // ----------------------------------------------------------------
            // Shift 键（左 Shift: 160, 右 Shift: 161）
            // ----------------------------------------------------------------
            // Shift 是修饰键，需要跟踪其状态
            160 | 161 => {
                SHIFT_PRESSED = is_keydown; // 更新 Shift 状态
                return "[shift]".to_string();
            }

            // ----------------------------------------------------------------
            // Ctrl 键（左 Ctrl: 162, 右 Ctrl: 163）
            // ----------------------------------------------------------------
            162 | 163 => {
                return "[ctrl]".to_string();
            }

            // ----------------------------------------------------------------
            // Alt 键（左 Alt: 164, 右 Alt: 165）
            // ----------------------------------------------------------------
            164 | 165 => {
                return "[alt]".to_string();
            }

            // ----------------------------------------------------------------
            // Caps Lock 键（虚拟键码: 0x14 = 20）
            // ----------------------------------------------------------------
            // Caps Lock 是切换键，每次按下都会切换状态
            VK_CAPITAL => {
                CAPS_LOCK_ON = !CAPS_LOCK_ON; // 切换 Caps Lock 状态
                return "[caps_lock]".to_string();
            }

            // ----------------------------------------------------------------
            // Windows 键（左 Win: 91, 右 Win: 92）
            // ----------------------------------------------------------------
            VK_LWIN => {
                return "[win]".to_string();
            }
            VK_RWIN => {
                return "[win]".to_string();
            }

            // ----------------------------------------------------------------
            // Enter/Return 键
            // ----------------------------------------------------------------
            VK_RETURN => {
                return "[return]".to_string();
            }

            // ----------------------------------------------------------------
            // 退格键
            // ----------------------------------------------------------------
            VK_BACK => {
                return "[backspace]".to_string();
            }

            // ----------------------------------------------------------------
            // Tab 键
            // ----------------------------------------------------------------
            VK_TAB => {
                return "[tab]".to_string();
            }

            // ----------------------------------------------------------------
            // 空格键
            // ----------------------------------------------------------------
            VK_SPACE => {
                return " ".to_string(); // 空格直接返回空格字符
            }

            // ----------------------------------------------------------------
            // Escape 键
            // ----------------------------------------------------------------
            VK_ESCAPE => {
                return "[esc]".to_string();
            }

            // ----------------------------------------------------------------
            // 方向键
            // ----------------------------------------------------------------
            VK_LEFT => {
                return "[left]".to_string();
            }
            VK_UP => {
                return "[up]".to_string();
            }
            VK_RIGHT => {
                return "[right]".to_string();
            }
            VK_DOWN => {
                return "[down]".to_string();
            }

            // ----------------------------------------------------------------
            // 编辑键
            // ----------------------------------------------------------------
            VK_INSERT => {
                return "[insert]".to_string();
            }
            VK_DELETE => {
                return "[delete]".to_string();
            }
            VK_HOME => {
                return "[home]".to_string();
            }
            VK_END => {
                return "[end]".to_string();
            }
            VK_PRIOR => {
                return "[page_up]".to_string();
            }
            VK_NEXT => {
                return "[page_down]".to_string();
            }

            // ----------------------------------------------------------------
            // 功能键 F1-F12
            // ----------------------------------------------------------------
            VK_F1 => {
                return "[f1]".to_string();
            }
            VK_F2 => {
                return "[f2]".to_string();
            }
            VK_F3 => {
                return "[f3]".to_string();
            }
            VK_F4 => {
                return "[f4]".to_string();
            }
            VK_F5 => {
                return "[f5]".to_string();
            }
            VK_F6 => {
                return "[f6]".to_string();
            }
            VK_F7 => {
                return "[f7]".to_string();
            }
            VK_F8 => {
                return "[f8]".to_string();
            }
            VK_F9 => {
                return "[f9]".to_string();
            }
            VK_F10 => {
                return "[f10]".to_string();
            }
            VK_F11 => {
                return "[f11]".to_string();
            }
            VK_F12 => {
                return "[f12]".to_string();
            }

            // ----------------------------------------------------------------
            // 其他特殊键
            // ----------------------------------------------------------------
            VK_SNAPSHOT => {
                return "[print_screen]".to_string();
            }
            VK_SCROLL => {
                return "[scroll_lock]".to_string();
            }
            VK_PAUSE => {
                return "[pause]".to_string();
            }
            VK_NUMLOCK => {
                return "[num_lock]".to_string();
            }

            // 不是特殊按键，继续处理
            _ => {}
        }

        // ====================================================================
        // 第二部分：处理普通字符按键（仅 Hook 方式）
        // ====================================================================
        // 对于普通按键（字母、数字、符号），使用 Windows API ToAscii 来转换
        // ToAscii 会考虑当前键盘布局和修饰键状态
        #[cfg(feature = "hook")]
        if is_keydown {
            // 创建键盘状态数组
            // 这个数组包含所有 256 个虚拟键的状态
            // 每个字节的最高位（0x80）表示键是否按下
            // 最低位表示键是否切换开启（如 Caps Lock）
            let mut key_state: [u8; 256] = [0; 256];

            // 获取当前键盘状态
            // GetKeyboardState 会填充 key_state 数组
            if let Some(get_keyboard_state) = g_GetKeyboardState {
                if get_keyboard_state(key_state.as_mut_ptr()) != 0 {
                    // 设置 Shift 键的状态
                    // 如果 Shift 被按下，设置对应位
                    if SHIFT_PRESSED {
                        key_state[160] = 0x80; // 0x80 表示键被按下
                    }

                    // 设置 Caps Lock 的状态
                    // 如果 Caps Lock 开启，设置对应位
                    if CAPS_LOCK_ON {
                        key_state[VK_CAPITAL as usize] = 0x01; // 0x01 表示切换开启
                    }

                    // 调用 ToAscii 将虚拟键码转换为 ASCII 字符
                    // 参数：
                    //   vk_code: 虚拟键码
                    //   0: 硬件扫描码（我们不需要，传 0）
                    //   key_state: 键盘状态数组
                    //   buffer: 输出缓冲区，用于存储转换后的字符
                    //   0: 标志位，0 表示正常转换
                    let mut buffer: [u16; 2] = [0; 2]; // 输出缓冲区，最多 2 个字符
                    if let Some(to_ascii) = g_ToAscii {
                        let result =
                            to_ascii(vk_code, 0, key_state.as_ptr(), buffer.as_mut_ptr(), 0);

                        // 返回值：
                        //   -1: 转换为死字符（如输入法状态）
                        //   0: 无法转换
                        //   1: 转换为 1 个字符
                        //   2: 转换为 2 个字符
                        if result > 0 {
                            // 转换成功，返回字符
                            let c = buffer[0] as u8 as char;
                            return c.to_string();
                        }
                    }
                }
            }
        }

        // 如果无法转换，返回空字符串
        // 这可能是因为：
        // 1. 按键释放事件（is_keydown = false）
        // 2. 无法识别的按键
        // 3. ToAscii 转换失败
        "".to_string()
    }
}
